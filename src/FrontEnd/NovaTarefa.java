    public void setSelectedDate() {//Método que seleciona a data limite selecionada para 7 dias depois da data de hoje
        LocalDateTime instant = LocalDateTime.now();
        Calendar calendarStart = Calendar.getInstance();
        horasLimiteComboBox.setSelectedIndex(instant.getHour());
        minutosLimiteComboBox.setSelectedIndex(instant.getMinute());
        calendarStart.set(instant.getYear(), instant.getMonthValue() - 1, instant.getDayOfMonth());
        calendarStart.add(Calendar.DAY_OF_MONTH, 7);
        dataLimiteChooserCombo.setSelectedDate(calendarStart);
    }

