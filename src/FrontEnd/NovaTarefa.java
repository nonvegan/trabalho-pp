    private void criarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_criarButtonActionPerformed
        if (!sistemaNovaTarefa.getCurrentSave().getListaProjetosSave().getListaProjetos().contains(projetoNovaTarefa)) {//Se projeto não for encontrada
            JOptionPane.showMessageDialog(this, "Este projeto não existe ou foi eliminado", "Erro ao criar tarefa.", JOptionPane.ERROR_MESSAGE);//Mensagem de erro
            dispose(); //dispose da janela
        }
        String erro = "";
        String prioridade = "";

        if (dataLimiteChooserCombo.getSelectedDate() != null) {
            if (LocalDateTime.of(dataLimiteChooserCombo.getSelectedDate().get(Calendar.YEAR), dataLimiteChooserCombo.getSelectedDate().get(Calendar.MONTH) + 1, dataLimiteChooserCombo.getSelectedDate().get(Calendar.DAY_OF_MONTH), horasLimiteComboBox.getSelectedIndex(), minutosLimiteComboBox.getSelectedIndex()).isBefore(LocalDateTime.now())) { //se data limite for antes da data atual
                erro += "Data Limite inválida (Data antes de Hoje).\n";
            }
            if (LocalDateTime.of(dataLimiteChooserCombo.getSelectedDate().get(Calendar.YEAR), dataLimiteChooserCombo.getSelectedDate().get(Calendar.MONTH) + 1, dataLimiteChooserCombo.getSelectedDate().get(Calendar.DAY_OF_MONTH), horasLimiteComboBox.getSelectedIndex(), minutosLimiteComboBox.getSelectedIndex()).isAfter(projetoNovaTarefa.getDataLimiteProjeto())){
                erro += "Data Limite inválida (Data após data limite do Projeto).\n";
            }
            
        } else {
            erro += "Data Limite inválida.\n";
        }

        if (títuloTextField.getText().isEmpty()) {//se o título estiver vazio, adicionar ao erro
            erro += "Nome inválido.\n";
            títuloLabel.setVisible(true);
        }
        if (utilizadorEncarregueValue.getText().isEmpty()) {//Se utilizador encarregue não tiver, adicionar ao erro
            erro += "Utilizador encarregue não selecionado\n";
            utilizadorEncarrregueEmptyLabel.setVisible(true);
        }
        switch (prioridadeSlider.getValue()) {//prioridade selecionada
            case 0:
                prioridade = "Baixa";
                break;
            case 1:
                prioridade = "Média";
                break;
            case 2:
                prioridade = "Alta";
                break;
        }
        if (jComboBox1.getSelectedIndex() == -1) {//se não tiver nenuma selecionada, adicionar ao erro
            erro += "Lista de Tarefas não selecionada\n";
            listaTarefasEmptyLabel.setVisible(true);
        }

        if (!erro.isEmpty()) {//se houver errors
            JOptionPane.showMessageDialog(this, erro, "Erro ao criar nova Tarefa", JOptionPane.ERROR_MESSAGE);//Mensagem de erro
        } else {//se não houver erros
            Tarefa novaTarefa = new Tarefa(títuloTextField.getText(), descriçãoEditorPane.getText(), sistemaNovaTarefa.getCurrentUser(), sistemaNovaTarefa.getCurrentSave().getListaUsersSave().getUserListaUsers(utilizadorEncarregueValue.getText().substring(utilizadorEncarregueValue.getText().indexOf("( ") + 2, utilizadorEncarregueValue.getText().indexOf(" )"))), prioridade, LocalDateTime.of(dataLimiteChooserCombo.getSelectedDate().get(Calendar.YEAR), dataLimiteChooserCombo.getSelectedDate().get(Calendar.MONTH) + 1, dataLimiteChooserCombo.getSelectedDate().get(Calendar.DAY_OF_MONTH), horasLimiteComboBox.getSelectedIndex(), minutosLimiteComboBox.getSelectedIndex()));//cria nova tarefa
            ListaTarefas listaTarefas = (ListaTarefas) jComboBox1.getSelectedItem();
            if (!projetoNovaTarefa.getListasTarefas().contains(listaTarefas)) {//se a lista não for encontrada
                JOptionPane.showMessageDialog(this, "A lista de tarefas que selecionou não exite ou foi eleminada", "Erro ao criar tarefa.", JOptionPane.ERROR_MESSAGE);//mensagem de erro
                dispose(); //dispose da janela
            } else {
                listaTarefas.addTarefaListaTarefas(novaTarefa);//adicionar a tarefa à lista selecionada
                sistemaNovaTarefa.setSaved(false);//Atualiza o estado do sistema como não gravado
                sistemaNovaTarefa.getDashboardWindow().refreshUI();//atualiza o estado
                dispose();//faz dispose da janela
            }
        }

    }//GEN-LAST:event_criarButtonActionPerformed

